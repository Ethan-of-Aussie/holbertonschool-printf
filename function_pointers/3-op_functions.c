#include "3-calc.h"

/**
 * op_add - Adds two numbers
 *
 * @a: Number 1
 *
 * @b: Number 2
 *
 * Return: Added result
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Subtracts two numbers
 *
 * @a: Number 1
 *
 * @b: Number 2
 *
 * Return: Subtracted result
 */


int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - Adds two numbers
 *
 * @a: Number 1
 *
 * @b: Number 2
 *
 * Return: Multiplied result
 */


int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Divides two numbers
 *
 * @a: Number 1
 *
 * @b: Number 2
 *
 * Return: Divided result
 */


int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Modular division between two numbers
 *
 * @a: Number 1
 *
 * @b: Number 2
 *
 * Return: Remainder
 */

int op_mod(int a, int b)
{
	return (a % b);
}
